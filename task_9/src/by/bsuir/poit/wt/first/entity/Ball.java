package by.bsuir.poit.wt.first.entity;

public class Ball {
    private final double weight;
    private final String color;

    public Ball(double weight, String color) {
        this.weight = weight;
        this.color = color;
    }

    public double getWeight() {
        return weight;
    }

    public String getColor() {
        return color;
    }
}
